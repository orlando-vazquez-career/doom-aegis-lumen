export const textWidth = (str, s, gap = 1) => str.length * (3 + gap) * s - gap * s;

export function drawText(ctx, x, y, str, s, color, gap = 1) {
  let cx = x;
  for (const ch of String(str).toUpperCase()) {
    const g = F[ch] || F[' '];
    ctx.fillStyle = color;
    for (let r = 0; r < 5; r++) for (let c = 0; c < 3; c++) if (g[r][c] === '1') ctx.fillRect(cx + c * s, y + r * s, s, s);
    cx += (3 + gap) * s;
  }
  return cx;
}

/** Texto con sombra dura 1px-escalada (acabado DOOM). */
export function drawTextShadow(ctx, x, y, str, s, color, shadow = '#000') {
  drawText(ctx, x + s, y + s, str, s, shadow);
  drawText(ctx, x, y, str, s, color);
}

export function drawCentered(ctx, cx, y, str, s, color, shadow) {
  const x = (cx - textWidth(str, s) / 2) | 0;
  if (shadow) drawTextShadow(ctx, x, y, str, s, color, shadow);
  else drawText(ctx, x, y, str, s, color);
}
